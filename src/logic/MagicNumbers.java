package logic;

public enum MagicNumbers {
    PNG(new byte[]{(byte) 0x89, 0x50, 0x4E, 0x47, 0x0D, 0x0A, 0x1A, 0x0A}, "png"),
    JPG(new byte[]{(byte) 0xFF, (byte) 0xD8, (byte) 0xFF}, "jpg"),
    GIF(new byte[]{0x47, 0x49, 0x46, 0x38, 0x37, 0x61}, "gif"),
    GIF2(new byte[]{0x47, 0x49, 0x46, 0x38, 0x39, 0x61}, "gif"),
    TIFF(new byte[]{0x49, 0x49, 0x2A, 0x00}, "tiff"),
    TIFF2(new byte[]{0x4D, 0x4D, 0x00, 0x2A}, "tiff"),
    BMP(new byte[]{0x42, 0x4D}, "bmp"),
    ZIP(new byte[]{0x50, 0x4B, 0x03, 0x04}, "zip"),
    RAR(new byte[]{0x52, 0x61, 0x72, 0x21, 0x1A, 0x07, 0x00}, "rar"),
    RAR2(new byte[]{0x52, 0x61, 0x72, 0x21, 0x1A, 0x07, 0x01, 0x00}, "rar"),
    RAR3(new byte[]{0x52, 0x61, 0x72, 0x21, 0x1A, 0x07, 0x00, 0x00}, "rar"),
    RAR4(new byte[]{0x52, 0x61, 0x72, 0x21, 0x1A, 0x07, 0x01, 0x00}, "rar"),
    AVI(new byte[]{0x52, 0x49, 0x46, 0x46}, "avi"),
    MKV(new byte[]{0x1A, 0x45, (byte) 0xDF, (byte) 0xA3}, "mkv"),
    MP3(new byte[]{0x49, 0x44, 0x33}, "mp3"),
    MOV(new byte[]{0x66, 0x74, 0x79, 0x70, 0x4D, 0x4F, 0x4F, 0x56}, "mov"),
    PDF(new byte[]{0x25, 0x50, 0x44, 0x46}, "pdf"),
    WMV(new byte[]{0x30, 0x26, (byte) 0xB2, 0x75, (byte) 0x8E, 0x66, (byte) 0xCF, 0x11, (byte) 0xA6, (byte) 0xD9, 0x00, (byte) 0xAA, 0x00, 0x62, (byte) 0xCE, 0x6C}, "wmv"),
    SEVENZ(new byte[]{0x37, 0x7A, (byte) 0xBC, (byte) 0xAF, 0x27, 0x1C}, "7z"),
    DOCX(new byte[]{0x50, 0x4B, 0x03, 0x04}, "docx"),
    EXE(new byte[]{0x5A, 0x4D}, "exe"),
    FLV(new byte[]{0x46, 0x4C, 0x56, 0x01}, "flv"),
    HTML(new byte[]{0x3C, 0x21, 0x44, 0x4F, 0x43, 0x54, 0x59, 0x50, 0x45, 0x20, 0x68, 0x74, 0x6D, 0x6C}, "html"),
    HTML2(new byte[]{0x3C, 0x21, 0x44, 0x4F, 0x43, 0x54, 0x59, 0x50, 0x45, 0x20, 0x78, 0x68, 0x74, 0x6D, 0x6C}, "hmtl"),
    MP4(new byte[]{0x00, 0x00, 0x00, 0x66, 0x74, 0x79, 0x70, 0x69, 0x73, 0x6F, 0x6D}, "mp4"),
    SWF(new byte[]{0x46, 0x57, 0x53}, "swf"),
    XLSX(new byte[]{0x50, 0x4B, 0x03, 0x04}, "xlsx"),
    XLS(new byte[]{(byte) 0xD0, (byte) 0xCF, 0x11, (byte) 0xE0, (byte) 0xA1, 0x1A, (byte) 0xE1, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00}, "xls"),
    ;
    private final byte[] magicBytes;
    private final String fileType;

    MagicNumbers(byte[] magicBytes, String fileType) {
        this.magicBytes = magicBytes;
        this.fileType = fileType;
    }

    public static MagicNumbers.MagicNumber[] getMagicNumbers() {
        return MagicNumbers.MagicNumber.values();
    }

    public record MagicNumber(byte[] magicBytes, String fileType) {
        public static MagicNumber[] values() {
            MagicNumber[] magicNumbers = new MagicNumber[MagicNumbers.values().length];
            for (int i = 0; i < MagicNumbers.values().length; i++) {
                magicNumbers[i] = new MagicNumber(MagicNumbers.values()[i].magicBytes, MagicNumbers.values()[i].fileType);
            }
            return magicNumbers;
        }
    }
}
